package org.glygen.tablemaker.view;

public class UserStatisticsView {
    Long glycanCount = 0L;
    Long collectionCount = 0L;
    Long cocCount = 0L;
    /**
     * @return the glycanCount
     */
    public Long getGlycanCount() {
        return glycanCount;
    }
    /**
     * @param glycanCount the glycanCount to set
     */
    public void setGlycanCount(Long glycanCount) {
        this.glycanCount = glycanCount;
    }
    /**
     * @return the collectionCount
     */
    public Long getCollectionCount() {
        return collectionCount;
    }
    /**
     * @param collectionCount the collectionCount to set
     */
    public void setCollectionCount(Long collectionCount) {
        this.collectionCount = collectionCount;
    }
    /**
     * @return the cocCount
     */
    public Long getCocCount() {
        return cocCount;
    }
    /**
     * @param cocCount the cocCount to set
     */
    public void setCocCount(Long cocCount) {
        this.cocCount = cocCount;
    }
    
    

}
